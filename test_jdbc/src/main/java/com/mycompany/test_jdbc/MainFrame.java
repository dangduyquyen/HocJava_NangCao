    private void jButton_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_saveActionPerformed
        String ID = jTextID.getText().toString();
        String hoTen = jTextHoTen.getText().toString();
        String diaChi = jTextDiaChi.getText().toString();
        String email = jTextEmail.getText().toString();
        // them vao list
        Student std = new Student(ID, hoTen, diaChi, email);
        studentList.add(std);
        
        // them du lieu vao csdl:
        // mo ket noi
        Connection conn = null;
        PreparedStatement preparedStatement = null;
        
       
        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/testjdbc", "root", "");
   
            //tao truy van - querry - lay du lieu + them du lieu + sua + xoa
                // select query
             
            String sql = "insert into student(ID, HoTen, DiaChi, Email) values(?,?,?,?)";
            
            
            preparedStatement = conn.prepareStatement(sql);
            preparedStatement.setString(1, std.ID);
            preparedStatement.setString(2, std.hoTen);
            preparedStatement.setString(3, std.diaChi);
            preparedStatement.setString(4, std.email);
            
            preparedStatement.execute();
            
        } catch (SQLException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
           // dong ket noi
            if(preparedStatement != null){
                try {
                    preparedStatement.close();
                } catch (SQLException ex) {
                    Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if(conn != null){
                try {
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            } 
        }
        
        
        // hien thi len table
        tableModel.addRow(new String[]{ID, hoTen, diaChi, email});
        // hien thi tren console
        System.out.println(ID + ";" + hoTen + ";" + diaChi + ";" + email);
       
      
        
        
    }//GEN-LAST:event_jButton_saveActionPerformed

