    private void jButtom_showActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtom_showActionPerformed
        Connection conn = null;
        Statement statement = null;
        List<Student> students = new ArrayList<>();
        try {
            // TODO add your handling code here:
            // mo ket moi toi dâtbase
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/testjdbc", "root", "");
             statement = conn.createStatement();
            //tao truy van - querry - lay du lieu + them du lieu + sua + xoa
            // select query
            String sql = "select * from student";
            ResultSet resultSet = statement.executeQuery(sql);
            
            
            while(resultSet.next()){
                Student std = new Student(
                        resultSet.getString("ID"), 
                        resultSet.getString("HoTen") ,
                        resultSet.getString("DiaChi"), 
                        resultSet.getString("Email"));
                students.add(std);
            }
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            // dong ket noi
            if(statement != null){
                try {
                    statement.close();
                } catch (SQLException ex) {
                    Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if(conn != null){
                try {
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        System.out.println("Size:  "+ students.size());
        for (Student student : students){
            System.out.println(student.toString());
            tableModel.addRow(new Object[]{
                student.getID(), student.getHoTen(), 
                student.getDiaChi(), student.getEmail()
            });
        }
        
        
        
    }//GEN-LAST:event_jButtom_showActionPerformed

